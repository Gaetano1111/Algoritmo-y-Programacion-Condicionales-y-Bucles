#include <stdio.h>
#include <stdlib.h>

int main()
{
    //Imprima los 10 numeros naturales usando el bucle while
    //Declare la variable i
    int i = 1;
    //se usa el while para imprimir los 10 numeros naturales
    //Y la condicion i<=10 para que el ciclo se detenga despues de haber impreso los 10 primeros numeros
    while(i <= 10){
        printf("%d\n",i);
     i++;
    }




    return 0;
}
